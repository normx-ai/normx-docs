def get_city_name(country_code: str, city_code: str) -> str:
    """
    Retourne le nom de la ville à partir du code
    """
    country_cities = OHADA_CITY_CODES.get(country_code, {})
    return country_cities.get(city_code.upper(), city_code)


def get_city_codes(country_code: str) -> dict:
    """
    Retourne tous les codes de villes pour un pays
    """
    return OHADA_CITY_CODES.get(country_code, {})


def validate_city_code(country_code: str, city_code: str) -> bool:
    """
    Vérifie si le code de ville est valide pour le pays
    """
    country_cities = OHADA_CITY_CODES.get(country_code, {})
    return city_code.upper() in country_cities